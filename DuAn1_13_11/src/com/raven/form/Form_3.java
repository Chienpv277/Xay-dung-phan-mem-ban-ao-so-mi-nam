    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        index = tblNhanVien.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Chọn 1 dòng để sửa!");
            return;
        }
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String ngaySinh = sdf.format(dpNgaySinh1.getDate());
        NhanVien nhanVien = getFormData2();
        NhanVien var = listNhanVien.get(index);
        int id = var.getId_nhan_vien();
        nhanVien.setId_nhan_vien(id);
        nhanVien.setNgay_sinh(var.getNgay_sinh());
        nhanVien.setCreated_at(var.getCreated_at());
        nhanVien.setHo_ten(txtTen.getText());
        nhanVien.setSdt(txtSdt.getText());
        nhanVien.setDia_chi(txtDiaChi.getText());
        if (cboChucVu.getSelectedIndex() == 0) {
            nhanVien.setChuc_vu(new ChucVu(1, null, null));
        } else {
            nhanVien.setChuc_vu(new ChucVu(2, null, null));
        }
        int confirmation = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa không?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (confirmation == JOptionPane.YES_OPTION) {
            String update = nhanVienService.update(nhanVien, id);
            JOptionPane.showMessageDialog(this, update);
            listNhanVien = nhanVienService.getAll();
            fillToTable(listNhanVien);
            clearForm();
        } else {
            clearForm();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

