    private NhanVien getFormData() {
        String id = null;
        String hoTen = txtTen1.getText();
        Date ngaySinh = dpNgaySinh1.getDate();
        String sdt = txtSdt1.getText();
        String soCanCuoc = txtSoCanCuoc2.getText();
        String email = txtEmail1.getText();
        String user = email;
        String password = String.valueOf(randomPass);
        String diaChi = txtDiaChi1.getText();
//        Date ngayTao = dpNgayTao.getDate();
        if (hoTen.equals("") || ngaySinh.equals("") || sdt.equals("") || email.equals("") || soCanCuoc.equals("")
                || diaChi.equals("")) {
            JOptionPane.showMessageDialog(this, "Không được để trống!");
            return null;
        }

        String patternsdt = "0\\d{9,10}";
        if (!this.txtSdt1.getText().matches(patternsdt)) {
            JOptionPane.showMessageDialog(this, "SDT không đúng định dạng!");
            return null;
        }

        String dangEmail = "\\w+@\\w+\\.\\w+";
        if (!email.matches(dangEmail)) {
            JOptionPane.showMessageDialog(this, "Email không đúng định dạng!");
            return null;
        }

        boolean isGioiTinh = rdoNam1.isSelected();
        String gioiTinh;
        if (isGioiTinh) {
            gioiTinh = "Nam";
        } else {
            gioiTinh = "Nữ";
        }

        boolean isTrangThai = rdoDangLam1.isSelected();
        int trangThai1;
        if (isTrangThai) {
            trangThai1 = 1;
        } else {
            trangThai1 = 2;
        }

        int chucVu = cboChucVu1.getSelectedIndex() + 1;
        ChucVu cv = new ChucVu(chucVu, null, null);

        NhanVien nv = new NhanVien(cv, hoTen, ngaySinh, sdt, soCanCuoc, gioiTinh, diaChi, email, user, password, trangThai1);
        return nv;
    }

    private void fillToTableNhanVienAn(List<NhanVien> listNhanVien) {
        model.setRowCount(0);
        int i = 1;
        for (NhanVien x : listNhanVien) {
            if (x.getTrang_thai() == 2) {
                model.addRow(new Object[]{i++, x.getHo_ten(), x.getNgay_sinh(), x.getSdt(), x.getEmail(), x.getCreated_at(), x.getDia_chi(), x.getSo_can_cuoc(), x.getGioi_tinh(), x.getChuc_vu().getTen(), x.getTrang_thai() == 1 ? "Đang làm" : "Nghỉ"});
            }
        }
    }

