    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        NhanVien nhanVien = getFormData();
        RandomString randomStr = new RandomString(9, ThreadLocalRandom.current());
        randomPass = randomStr.nextString();
        if (nhanVien == null) {
            return;
        }
        boolean isTrangThai = rdoDangLam1.isSelected();
        int trangThai1;
        if (isTrangThai) {
            trangThai1 = 1;
        } else {
            JOptionPane.showMessageDialog(this, "Nhân viên mới phải trạng thái đang làm");
            return;
        }

        for (NhanVien x : listNhanVien) {
            if (x.getEmail().equalsIgnoreCase(txtEmail1.getText())) {
                JOptionPane.showMessageDialog(this, "Email đã tồn tại");
                return;
            }
        }

        for (NhanVien x : listNhanVien) {
            if (x.getSdt().equalsIgnoreCase(txtSdt1.getText())) {
                JOptionPane.showMessageDialog(this, "Số điện thoại đã tồn tại");
                return;
            }
        }
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String ngaySinh = sdf.format(dpNgaySinh1.getDate());
        nhanVien.setPassword(randomPass);
        nhanVien.setNgay_sinh(dpNgaySinh1.getDate());
        int confirmation = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm nhân viên không?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (confirmation == JOptionPane.YES_OPTION) {
            String add = nhanVienService.add(nhanVien);
            if (add.equals("Thêm thành công")) {
                JOptionPane.showMessageDialog(this, add);
                listNhanVien = nhanVienService.getAll();
                jPanel1.setVisible(true);
                jPanel4.setVisible(false);
                fillToTable(listNhanVien);
                sendPass(randomPass);
            } else {
                JOptionPane.showMessageDialog(this, add);
            }
        } else {
            jPanel2.setVisible(true);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    public void fillToTable(List<NhanVien> listNhanVien) {
        int i = 1;
        model.setRowCount(0);
        for (NhanVien x : listNhanVien) {
            model.addRow(new Object[]{i++, x.getHo_ten(), x.getNgay_sinh(), x.getSdt(), x.getEmail(), x.getCreated_at(), x.getDia_chi(), x.getSo_can_cuoc(), x.getGioi_tinh(), x.getChuc_vu().getTen(), x.getTrang_thai() == 1 ? "Đang làm" : "Nghỉ"});
        }
    }

    private void fillToForm() {
        index = tblNhanVien.getSelectedRow();
        listNhanVien = nhanVienService.getAll();
        if (index == -1) {
            return;
        }
        NhanVien var = listNhanVien.get(index);
        txtTen.setText(var.getHo_ten());
        try {
            Date date = new SimpleDateFormat("yyyy-MM-dd").parse(String.valueOf(var.getNgay_sinh()));
            dpNgaySinh1.setDate(date);
            System.out.println(date);
        } catch (Exception e) {
            e.printStackTrace();
        }
        txtSdt.setText(var.getSdt());
        txtSoCanCuoc.setText(var.getSo_can_cuoc());
        txtEmail.setText(var.getEmail());
        dpNgayTao.setDate(var.getCreated_at());
        txtDiaChi.setText(var.getDia_chi());
        if (tblNhanVien.getValueAt(index, 9).toString().equalsIgnoreCase("Chủ")) {
            cboChucVu.setSelectedIndex(0);
        } else {
            cboChucVu.setSelectedIndex(1);
        }
        if (tblNhanVien.getValueAt(index, 10).toString().equalsIgnoreCase("Đang làm")) {
            rdoDangLam.setSelected(true);
        } else {
            rdoNghi.setSelected(true);
        }

        if (tblNhanVien.getValueAt(index, 8).toString().equalsIgnoreCase("Nam")) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
        tblNhanVien.setRowSelectionInterval(index, index);
    }

